from helpers import timer
import functools
import itertools


@timer
def part1(slope):
    right, down = slope
    treeCount = 0
    width = len(lines[0])
    x, y = 0, 0
    while y < len(lines):
        if lines[y][x] == "#":
            treeCount += 1
        x += right
        x = x % width
        y += down
    return treeCount


@timer
def part2():
    return functools.reduce(
        lambda x, y: x * y,
        [part1(slope) for slope in [(1, 1), (3, 1), (5, 1), (7, 1), (1, 2)]],
    )


